    p.chart_id,
    COALESCE(p.facility_confirmed, 0) as facility_confirmed,
    p.chart_notes,
    COALESCE(p.intake_call_completed, 0) as intake_call_completed,
    p.intake_notes,
    p.initial_tv_completed_date as task_date,
    p.goc_value
FROM patients p
    LEFT JOIN (
        SELECT patient_id,
            MAX(provider_id) AS provider_id,
            MAX(coordinator_id) AS coordinator_id
        FROM patient_assignments
        WHERE provider_id > 0
            OR coordinator_id > 0
        GROUP BY patient_id
    ) pa ON pa.patient_id = p.patient_id
    LEFT JOIN users up ON pa.provider_id = up.user_id
    LEFT JOIN users uc ON pa.coordinator_id = uc.user_id;